package domain.players;

/**
 * Enumeracion que define los sabores disponibles para los personajes del juego.
 * Cada sabor determina la apariencia visual (color y sprite) del jugador.
 * 
 * <p>Sabores disponibles:</p>
 * <ul>
 *   <li>VANILLA - Helado de vainilla (color crema)</li>
 *   <li>CHOCOLATE - Helado de chocolate (color marron)</li>
 *   <li>STRAWBERRY - Helado de fresa (color rosa)</li>
 * </ul>
 * 
 * @author Diego Montes y Juan David Valero
 * @version 1.0
 * @see IceCream
 */
public enum IceCreamFlavor {

    /** Sabor vainilla - Color crema claro */
    VANILLA,

    /** Sabor chocolate - Color marron */
    CHOCOLATE,

    /** Sabor fresa - Color rosa */
    STRAWBERRY
}
